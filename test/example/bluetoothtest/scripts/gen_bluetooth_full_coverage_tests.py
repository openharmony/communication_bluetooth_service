def split_chunks(chunks: Sequence[Chunk], max_per_file: int) -> List[List[Chunk]]:
    out: List[List[Chunk]] = []
    cur: List[Chunk] = []
    for c in chunks:
        cur.append(c)
        if len(cur) >= max_per_file:
            out.append(cur)
            cur = []
    if cur:
        out.append(cur)
    return out


