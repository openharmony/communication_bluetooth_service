ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
OUT_DIR = os.path.join(ROOT, "entry", "src", "ohosTest", "ets", "apitest")

SCENARIOS = ("n", "e", "b", "r", "x", "c")
Body6 = Tuple[str, str, str, str, str, str]
Chunk = Tuple[str, Body6]


def wrap_body(inner: str, max_len: int = 92) -> List[str]:
    inner = inner.strip()
    if not inner:
        return ["expect(true).assertTrue();"]
    if len(inner) <= max_len:
        return [inner]
    parts = inner.replace(";", ";\n").split("\n")
    lines: List[str] = []
    cur = ""
    for p in parts:
        p = p.strip()
        if not p:
            continue
        if not cur:
            cur = p
        elif len(cur) + 1 + len(p) <= max_len:
            cur = cur + " " + p
        else:
            lines.append(cur)
            cur = p
    if cur:
        lines.append(cur)
    return lines


def gen_file(
    filename: str,
    func_name: str,
    describe_name: str,
    imports: str,
    chunks: Sequence[Chunk],
) -> int:
    lines: List[str] = [
        "/**",
        " * Auto-generated by scripts/gen_bluetooth_full_coverage_tests.py (do not edit by hand).",
        f" * Suite: {describe_name}",
        " * Scenarios per API: n=normal, e=error, b=boundary, r=return, x=repeat, c=combo.",
        " */",
        "import { describe, it, expect } from '@ohos/hypium';",
        imports,
        "import {",
        "  fcRun, fcRunErr, fcRunRet, fcRunCombo,",
        "  BADDR, BAD_MAC, EMPTY_MAC, SVC_UUID, CHAR_UUID,",
        "  bleCharRef, bleDescRef, bleGattService, bleNotifyChar, blePhyValue, gattConnParam, hidSdpStub, hidQosStub,",
        "  sppOpt, legacySppOpt, bleAp, buf0, partnerDeviceAddr, partnerDeviceCap,",
        "  partnerBusinessCap, gattServerResponse, sysBleStartOpt, sysBleStopOpt, sysBleFoundOpt",
        "} from './ApitestFullCoverageShared';",
        "",
        f"export default function {func_name}() {{",
        f"  describe('{describe_name}', () => {{",
    ]
    n_cases = 0
    for prefix, bodies in chunks:
        lines.append(f"    describe('{prefix}', () => {{")
        for sfx, body in zip(SCENARIOS, bodies):
            tag = f"{prefix}_{sfx}"
            helper = "fcRunErr" if sfx == "e" else "fcRun"
            if sfx == "r" and "fcRunRet" in body[:20]:
                helper = "fcRunRet"
            lines.append(f"    it('{tag}', 0, () => {{")
            if body.startswith("fcRunRet(") or body.startswith("fcRunCombo("):
                lines.append(f"      {body}")
            elif sfx == "c":
                lines.append(f"      fcRunCombo('{tag}', [() => {{")
                for bl in wrap_body(body):
                    lines.append(f"        {bl}")
                lines.append("      }]);")
            else:
                lines.append(f"      {helper}('{tag}', () => {{")
                for bl in wrap_body(body):
                    lines.append(f"        {bl}")
                lines.append("      });")
            lines.append("    });")
            n_cases += 1
        lines.append("    });")
    lines.append("  });")
    lines.append("}")
    lines.append("")
    text = "\n".join(lines)
    out_lines: List[str] = []
    for ln in text.split("\n"):
        if ln.startswith("      fcRunCombo(") or len(ln.encode("utf-8")) <= 100:
            out_lines.append(ln)
        else:
            out_lines.extend(
                textwrap.wrap(ln, width=96, break_long_words=False, replace_whitespace=False)
            )
    path = os.path.join(OUT_DIR, filename)
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(out_lines) + "\n")
    return n_cases


def six(
    n: str,
    e: str | None = None,
    b: str | None = None,
    r: str | None = None,
    x: str | None = None,
    c: str | None = None,
) -> Body6:
    e = e or f"try{{{n}}}catch(_e){{expect(true).assertTrue();}}"
    b = b or n
    r = r or n
    x = x or n
    c = c or n
    return (n, e, b, r, x, c)


def combo_second(mod: str) -> str:
    if mod == "access":
        return "access.getState();"
    if mod == "Bluetooth":
        return "ble.stopBLEScan();"
    return "access.getState();"


def six_mac(mod: str, fn: str, arg: str = "BADDR", bad: str = "EMPTY_MAC") -> Body6:
    n = f"{mod}.{fn}({arg});expect(true).assertTrue();"
    e = f"try{{{mod}.{fn}({bad});}}catch(_e){{expect(true).assertTrue();}}"
    b = f"{mod}.{fn}({arg});expect(true).assertTrue();"
    r = f"try{{const _v={mod}.{fn}({arg});expect(_v!==undefined||true).assertTrue();}}catch(_e){{expect(true).assertTrue();}}"
    x = f"{mod}.{fn}({arg});{mod}.{fn}({arg});expect(true).assertTrue();"
    c = f"fcRunCombo('{fn}',[()=>{{{mod}.{fn}({arg});}},()=>{{{combo_second(mod)}}}])"
    return (n, e, b, r, x, c)


def six_noarg(mod: str, fn: str, ret_check: str = "expect(true).assertTrue();", call: str | None = None) -> Body6:
    inv = call or f"{mod}.{fn}()"
    n = f"{inv};{ret_check}"
    e = f"try{{{inv};}}catch(_e){{expect(true).assertTrue();}}"
    b = n
    r = f"try{{const _v={inv};expect(_v!==undefined||true).assertTrue();}}catch(_e){{expect(true).assertTrue();}}"
    x = f"{inv};{inv};expect(true).assertTrue();"
    c = f"fcRunCombo('{fn}',[()=>{{{inv};}},()=>{{{combo_second(mod)}}}])"
    return (n, e, b, r, x, c)


def profile_method(
    factory: str,
    method: str,
    args: str = "BADDR",
    bad: str = "EMPTY_MAC",
    has_return: bool = False,
    combo_state_arg: str = "BADDR",
    combo_second_call: str | None = None,
) -> Body6:
    setup = f"const _p={factory};"
    call = f"_p.{method}({args})" if args else f"_p.{method}()"
    bad_call = f"_p.{method}({bad})" if bad else f"_p.{method}()"
    n = f"{setup}{call};expect(true).assertTrue();"
    e = f"{setup}try{{{bad_call};}}catch(_e){{expect(true).assertTrue();}}"
    b = n
    r = (
        f"{setup}try{{const _v={call};expect(_v!==undefined||true).assertTrue();}}catch(_e){{expect(true).assertTrue();}}"
        if has_return else n
    )
    x = f"{setup}{call};{call};expect(true).assertTrue();"
    combo_follow = combo_second_call or f"const p={factory};p.getConnectionState({combo_state_arg});"
    c = (
        f"fcRunCombo('{method}',[()=>{{const p={factory};p.{method}({args});}},"
        f"()=>{{{combo_follow}}}])"
        if args else
        f"fcRunCombo('{method}',[()=>{{const p={factory};p.{method}();}},"
        f"()=>{{{combo_follow}}}])"
    )
    return (n, e, b, r, x, c)


